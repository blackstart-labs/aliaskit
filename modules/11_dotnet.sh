#!/usr/bin/env bash
# CATEGORY: .NET
# MODULE: dotnet

## dn
# @desc  DotNet shorthand
# @usage dn
# @example dn
alias dn='dotnet'

## dnb
# @desc  Build dotnet project
# @usage dnb
# @example dnb
alias dnb='dotnet build'

## dnr
# @desc  Run dotnet project
# @usage dnr
# @example dnr
alias dnr='dotnet run'

## dnc
# @desc  Clean dotnet project
# @usage dnc
# @example dnc
alias dnc='dotnet clean'

## dnt
# @desc  Run dotnet tests
# @usage dnt
# @example dnt
alias dnt='dotnet test'

## dnadd
# @desc  Add package to project
# @usage dnadd <pkg>
# @example dnadd Newtonsoft.Json
alias dnadd='dotnet add package'
